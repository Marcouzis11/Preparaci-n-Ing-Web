package Interfaces;
/*
 * # Para utilizar la interfaz, se utiliza "implements nombreInterfaz".
 * # La claseNoInterfaz, va a tener que hacer el código para cada uno de los métodos abstractos de la interfaz.
 * # 
*/
public class claseNoInterfaz implements interfaz {
    claseNoInterfaz()
    {

    }

    public void hacerAlgo()
    {
        System.out.println("Hace algo");
    }

    public void hacerAlgo2()
    {
        System.out.println("Hace algo 2");
    }

    public void hacerAlgo3()
    {
        System.out.println("Hace algo 3");
    }
}
